	std::string getPath(const std::string& name, const fileType& type)
	{
		std::string returnPath = PROJECT_SOURCE_DIR;
		if (returnPath.find_last_of("/") == returnPath.length() - 1 || returnPath.find_last_of("\\") == returnPath.length() - 1)
		{
			returnPath = returnPath.substr(0, returnPath.length() - 1);
		}

		switch (type)
		{
		case fileType::HEADER:
			returnPath += "/Headers/";
			break;
		case fileType::TEXTURE:
			returnPath += "/Textures/";
			break;
		case fileType::SHADER:
			returnPath += "/Shaders/";
			break;
		case fileType::SOURCE:
			returnPath += "/Sources/";
			break;
		default:
			return "";
			break;
		}

		std::cout << "getPath " << name << " = " << returnPath + name << std::endl;
		return returnPath + name;
	}

